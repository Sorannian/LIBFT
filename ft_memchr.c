#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*s2;
	size_t	i;

	s2 = (char *)s;
	i = 0;
	while (s2[i] != c && s2[i] != '\0' && i < n)
		s2++;
	if (s2[i] == c)
		return ((char *)s2);
	return (NULL);
}
